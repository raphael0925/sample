package hellojava2;

public class expression1101 {

	public static void main(String[] args) {
		
		int age;
		age = 100;
		System.out.println(age);
		
		float number;
		number = 100.12f;
		System.out.println(number);

		double myNumber;
		myNumber = 100.12;
		System.out.println(myNumber);

		System.out.println("100");

		boolean a;
		a = true;
		System.out.println(a);
		
		boolean b;
		b = false;
		System.out.println(b);

	}

}
